                    <h1>Our History</h1>
                    <p>
                        Akosombo International School was established in 1962 to provide educational facilities at the
                        Kindergarten and Primary levels for the children of workers (mostly expatriates), who were
                        engaged
                        in the construction of the Akosombo dam.
                        <br>
                        The School, at its inception, was an interesting experiment in education. Of the 140 students
                        who
                        were enrolled in the school at the time, only 28 (i.e. 20%), were Ghanaians.
                        <br>
                        The rest were foreigners from such diverse countries as Italy, United States of America, United
                        Kingdom, Australia, Canada, Pakistan, the Netherlands, Brazil, and Portugal. All of these
                        foreign
                        students received tuition at AIS that enabled them to write examinations in their home
                        countries.
                        <br>
                        With the completion of the Akosombo Dam project in 1966, the Akosombo International School was
                        opened to the children/wards of all employees of the Volta River Authority. In 1968, a regular
                        co-educational secondary department was added to the existing nursery and primary schools.
                        Facilities were put in place to accommodate boarders. At its establishment, the secondary school
                        department was intended to follow technical courses in order to take advantage of the numerous
                        facilities in Akosombo for such pursuits (e.g. carpentry workshops, vehicle workshop, mechanical
                        workshop, etc.) However, other courses such as General Science, General Arts, Home Economics and
                        Business have been added to the School’s curriculum. In September 1990, a two-stream Junior
                        Secondary School Department was introduced at Akosombo; and January 1994 saw the rst intake of
                        the
                        three-stream Senior Secondary School in preparation for the phasing-out of the then existing
                        secondary school system.
                        <br>
                        The year 1982 saw the further expansion of the facilities of the School with the opening of the
                        Akuse Kindergarten and Primary Schools, as the second stream of the Akosombo International
                        School.
                        The Akuse School was established to cater for the children/wards of VRA sta in charge of the
                        hydro-electric facilities at Akuse. A Junior Secondary School department commenced at Akuse in
                        September 2002.
                        <br>
                        Kyease Kindergarten was set up to prepare pupils adequately before entering the public primary
                        schools in Akosombo. In September 2002, however, the Kyease kindergarten was expanded into a
                        primary
                        school due to increase in the population of the Akosombo township and the consequent increasing
                        demand for admission to the primary school at the main campus (i.e. at Community One).
                        <br>
                        In the mid-1990s, the VRA started work on its thermal power project at Aboadze, near Takoradi. A
                        new
                        township was built for sta at Aboadze; and in November 1999, a one-stream Kindergarten and
                        Primary
                        School was established at the VRA compound to provide educational facilities for the new
                        community.
                        A Junior Secondary department commenced at Aboadze in September 2001.
                        <br>
                        The first headmaster of the rst School at Akosombo was Mr. W.H.D. Ames, a Canadian. He was
                        succeeded
                        by other expatriate headmasters, i.e. Mr. J.B. Morris, September 1963 to August 1966, and Mr.
                        B.A.
                        Sea-man, August 1966 to July 1968. The first Ghanaian headmaster was the late Mr. P.D. Quartey
                        Jnr.,
                        who from September 1968 until his retirement in December 1973 worked very hard to establish on a
                        rm
                        basis the Secondary Department of the school. The second Headmaster, Mr. B.A. Sam, was at post
                        from
                        January 1974 until his retirement in December 1998. Under Mr. B.A. Sam, Mr. N. Ohene-Okae and
                        Mrs.
                        G. Manieson-Annancy served as Assistant Heads responsible for Basic and Secondary Schools
                        respectively. Mrs. G. Manieson-Annancy assumed duty as Headmistress from January 1999; and
                        Messrs
                        John K. Tawiah and Arnold K. Seshie were appointed as Assistant Heads.
                        <br>
                        Mr. N. Ohene-Okae (Assistant Headmaster) exercised oversight over both the Akosombo and Akuse
                        Basic
                        Schools. Ms. Mary Maud Beausoleil was Supervisor at Akuse from its inception until her
                        retirement in
                        1995. Mr. Dan Cole was appointed Supervisor from September 1995 until his retirement in December
                        2000. Mr. Reuben Kugblenu is currently the Supervisor of the Akuse Basic Schools.
                        <br>
                        At the inception of the Aboadze Basic Schools in 1999, Mr. Arnold K. Seshie (Asst.Headmaster)
                        exercised oversight over all VRA Basic Schools. At the Aboadze campus, however, is Mr. Seth O.
                        Oppong, who has been Ag. Supervisor since November, 1999.
                        <br>
                        In 2001, the VRA Schools were elevated to the status of a fully-edged department within the VRA
                        set-up. Mrs. Grace Manieson-Annancy was appointed as the Director of the VRA Schools Department.
                        In
                        2003 two Headmasters, Messrs John K. Tawiah (Senior Secondary School) and Arnold K. Seshie
                        (Basic
                        Schools), were appointed to assist the Director in the administration of the Schools.
                        <br>
                        Eective January 2011, the head of the VRA Schools re-designated General Manager, with Mr. Arnold
                        Seshie occupying the position and Mr. Julius Kog-Der appointed Headmaster, Senior High School,
                        with
                        Mr. Shawn Kuwornu as Headmaster, Basic School Akosombo. Mr. Willis Ampiaw succeeded Mr. Kuwornu
                        in
                        2014, as Headmaster, Basic School, Akosombo. A new General Manager, Mrs. Bevelyn Asamoah, was
                        appointed to head the VRA Schools in December 2015 to date, after Mr. Seshie’s retirement.
                    </p>
