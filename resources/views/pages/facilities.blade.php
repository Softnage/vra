                    <h5>The Len Allen Memorial Library:
                    </h5>
                    <p>
                        The late Mr. Len Allen was a Canadian Engineering Consultant. He was a man of many humane
                        qualities.
                        He first came to Ghana in 1952 as a member of a team of engineers from Sir William Halcrow and
                        Partners who were in charge of the Volta River Project. Prominent among the tasks performed by
                        Len
                        Allen was the delineation of the proposed Volta River Reservoir, and the establishment of a
                        network
                        of permanent river gauges. After completion of Hydro-electric projects in other parts of the
                        world,
                        he later returned to Ghana as the Resident Manager of Acres International Engineering
                        Consultants
                        who were involved in the construction of the Kpong-Akuse Hydro-Electric Plant. During his stay
                        at
                        Akuse, Mr. Allen saw to the planting of fruit trees and the beautification of Akuse.
                        <br>
                        <br>
                        Len Allen finally returned to his home country (Canada) in July 1982, after having been awarded
                        the
                        Grand Medal of Ghana (Civil Division) by the Ghana Government. He received a similar award from
                        the
                        Canadian government. He died on May 4, 1983, in Toronto, Canada. It was in recognition of his
                        services to the School that the library was named after him. There is also the Allen Memorial
                        fund
                        which has been instituted in memory of Len Allen. The fund was established with the
                        contributions
                        and donations made by sympathisers at the memorial service held in honour of Mr. Len Allen in
                        Toronto, Canada. The accruing interest from the fund is used to replenish the stock of literary
                        material in the library. There is also the Len Allen Physical Science Awards Scheme that gives
                        awards to deserving students.
                        <br>
                        <br>
                        The library is well-stocked with a variety of literature, including textbooks, encyclopedias,
                        periodicals, journals and national dailies, among others. The Library offers Reference and
                        Lending
                        Services to the staff and students of Akosombo International School. However, adults and
                        students
                        from other institutions may be offered Reference services during vacation. The Libraries at
                        Akuse
                        and Aboadze schools are similarly organised.
                    </p>
                    <h5> The Book Shop:</h5>
                    <p>
                        The department has a Book Shop with satellite ones which offer for sale materials like
                        textbooks,
                        stationery and school uniforms. As much as possible, all students are supplied with government
                        textbook
                        and billed.
                        The cost of every item given out to students from the Bookshop is written into a Student’s Order
                        Book,
                        and the amount added to their bills.
                    </p>
                    <h5> Medical Care:</h5>
                    <p>
                        The V.R.A. Hospital and the clinics in the VRA townships provide for the medical needs of
                        students.
                        Students at the Boarding House in Akosombo who require medical attention must obtain a chit from
                        their Senior Housemaster/mistress in the morning before leaving for the hospital. A School
                        vehicle
                        conveys students to the hospital. Apart from this, the Parent –Teacher Association (PTA) has
                        purchased a mini-bus to convey students to and from the hospital. Furthermore, the VRA and the
                        Akosombo PTA have provided sick-bays in both the Boys and Girls dormitories where students
                        convalescing could conveniently be isolated
                        and monitored until recovery. The medical expenses of all students who are cared for at the
                        V.R.A.
                        Hospital and clinics are borne by Parents/Guardians. Such expenses are placed as an additional
                        item
                        on the students' bills.
                    </p>
                    <h5>School Canteens:</h5>
                    <p>
                        The VRA and the PTAs have provided canteens on the various campuses where Day students, in
                        particular, and the general student-body could take their meals and snacks during school breaks.
                        Various dishes are sold by vendors upon the recommendations of the Schools’ food committees.
                        Vendors
                        undergo regular medical examination.
                    </p>

                    <h5> Transport Facilities for Students:</h5>
                    <p>
                        All day students resident in Akosombo, Akuse, and Aboadze are transported to and from school
                        every
                        school day. The students who use the bus service pay a user-fee which is worked out and added to
                        their school bills.
                    </p>
                    <h5> Bus Service at the Beginning, Mid and End of Term:</h5>
                    <p>
                        Most boarding students of AIS come to Akosombo from Accra and Tema. During holidays, the school
                        buses take students home to Accra- Tema, and bring them back to school after the mid-term break.
                        at
                        the beginning and end of term. Students are charged for this bus service.
                    </p>
                    <h5>Excursions/Educational/Pleasure Trips:
                    </h5>
                    <p>
                        Students who as part of the schools own programmes, go on field trips outside the school are
                        provided transport. Clubs and Societies that plan for educational and excursion trips outside
                        the
                        school as part of their termly programme are required to pay the actual transport fare.
                    </p>
